package tarefas.entidades;

public class Tarefas {
	
	private String nomeTarefa;
	private String urgenciaTarefa;
	private String dificuldadeTarefa;
	private String descricaoTarefa;
	private String tempoTarefa;
	
	
	public String getNome_tarefa() {
		return nomeTarefa;
	}
	public void setNome_tarefa(String nome_tarefa) {
		this.nomeTarefa = nome_tarefa;
	}
	public String getUrgencia_tarefa() {
		return urgenciaTarefa;
	}
	public void setUrgencia_tarefa(String urgencia_tarefa) {
		this.urgenciaTarefa = urgencia_tarefa;
	}
	public String getDificuldade_tarefa() {
		return dificuldadeTarefa;
	}
	public void setDificuldade_tarefa(String dificuldade_tarefa) {
		this.dificuldadeTarefa = dificuldade_tarefa;
	}
	public String getDescricao_tarefa() {
		return descricaoTarefa;
	}
	public void setDescricao_tarefa(String descricao_tarefa) {
		this.descricaoTarefa = descricao_tarefa;
	}
	public String getTempo_tarefa() {
		return tempoTarefa;
	}
	public void setTempo_tarefa(String tempo_tarefa) {
		this.tempoTarefa = tempo_tarefa;
	}
	
	

	
	
}
