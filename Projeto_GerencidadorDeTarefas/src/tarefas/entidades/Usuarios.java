package tarefas.entidades;

public class Usuarios {
	private String[] bancoUsuarios = new String[1];
	private String[] bancoSenhas = new String[1];
	
	public String[] getBancoUsuarios() {
		return bancoUsuarios;
	}
	public void setBancoUsuarios(String[] bancoUsuarios) {
		this.bancoUsuarios = bancoUsuarios;
	}
	public String[] getBancoSenhas() {
		return bancoSenhas;
	}
	public void setBancoSenhas(String[] bancoSenhas) {
		this.bancoSenhas = bancoSenhas;
	}
	

}
